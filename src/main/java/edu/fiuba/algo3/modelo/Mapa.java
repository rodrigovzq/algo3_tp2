package edu.fiuba.algo3.modelo;

public class Mapa {
    private Integer ancho;
    private Integer altura;

    public Mapa(Integer ancho, Integer altura) {
        this.ancho = ancho;
        this.altura = altura;
    }

    public Integer getAncho() {
        return ancho;
    }

    public Integer getAltura() {
        return altura;
    }
}
